  HAL_TIM_IC_Start(&htim1, TIM_CHANNEL_1);		// Input Capture modunu başlat

  // TIM2 APB2 hattina bagli ve 90MHz hızında, prescalar (90-1) ile TIM2 1MHz'e düşürüldü
  // ARR 100 ile frekans 10KHz hızına düşürüldü
  // DAC Sinüs dalgasısının frekansı = 10KHz / SAMLENUM.
  HAL_TIM_Base_Start(&htim2);
