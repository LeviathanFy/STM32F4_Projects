/* USER CODE BEGIN 0 */
void sinGet (void)
{
	for (int i=0; i<SAMPLENUM; i++)
	{
		// Formul user manual -> application notes -> Using DAC to gen. sine wave basligindan alindi
		sine_arr[i] = (sin(i*2*PI/SAMPLENUM)+1)* (4096/2);
	}
}

void delay (uint16_t time)
{
	__HAL_TIM_SET_COUNTER(&htim1, 0);
	while (__HAL_TIM_GET_COUNTER(&htim1) < time);
}

// Timer Input Capture BOTH EDGES olarak seçildi
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)		// Timer üzerinde Kanal 1 yapılandırıldı.
	{
		if (Is_first)
		{
			InputCapture[0] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			Is_first = 0;
		}
		else
		{
			InputCapture[1] = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			__HAL_TIM_SET_COUNTER(htim, 0);


			if (InputCapture[0] > InputCapture[1])
			{
				Distance = (0xFFFF - InputCapture[0]) + InputCapture[1];
			}
			else
			{
				Distance = InputCapture[1] - InputCapture[0];
			}
			Distance = Distance * (0.034/2);	// Sesin havadaki hızı 34cm/ms (340m/s). Distance = (Hız x Zaman) / 2
			Is_first = 1;						// Timer ms birimine sahip olacak şekilde yapılandırıldı.
		}
	}
}

// Eğer ARR değerinin değiştirildiği an sayaç girilen ARR değerinden büyük ise DAC özelinde dengesizlik yaratmakta.
// EGR register'ı değiştirilerek sonraki sayaç döngüsünde girlen ARR değerinin aktif olması sağlandı.
void updateARR  (TIM_HandleTypeDef* htim, uint16_t arr)
{
    __HAL_TIM_SET_AUTORELOAD(htim, arr);

    if (__HAL_TIM_GET_COUNTER(htim) >= __HAL_TIM_GET_AUTORELOAD(htim))
    	htim->Instance->EGR  |= TIM_EGR_UG;
}

void HCSR04_read (void)
{
	HAL_GPIO_WritePin(GPIOG, GPIO_PIN_2, GPIO_PIN_SET);
	delay (10);		// 10us boyunca TRIGGER sinyal gönder
	HAL_GPIO_WritePin(GPIOG, GPIO_PIN_2, GPIO_PIN_RESET);
 }
